using ZR.Business.Services.Interfaces;
using ZR.Infrastructure.Models;
using Microsoft.AspNetCore.Diagnostics;
using System.Net;

namespace ZR.Api.Extensions
{
    public static class ErrorHandlerMiddleware
    {


        public static void ConfigureExceptionHandler(this IApplicationBuilder app, ILoggerManagerService logger)
        {
            app.UseExceptionHandler(appError =>
            {
                appError.Run(async context =>
                {
                    context.Response.StatusCode = (int)HttpStatusCode.InternalServerError;
                    context.Response.ContentType = "application/json";

                    IExceptionHandlerFeature? contextFeature = context.Features.Get<IExceptionHandlerFeature>();
                    if (contextFeature != null)
                    {
                        logger.LogError($"Something went wrong: {contextFeature.Error}");

                        await context.Response.WriteAsync(new ErrorDetails()
                        {
                            StatusCode = context.Response.StatusCode,
                            Message = "Internal Server Error."
                        }.ToString());
                    }
                });
            });
        }
    }
}
